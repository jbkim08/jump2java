public class HelloWorld {
    //    주석 Ctrl+/ (단축키)
    //    클래스는 자바프로그래밍 기본이고 이름은 대문자 시작
    public static void main(String[] args) {
        //    메인 메서드 : 프로그램 시작과 끝을 관리하는 함수
        System.out.println("출력하는 문자열!");
        for (int i = 0; i < 3; i++) {
            for (int j = 0; j < 4; j++) {
                System.out.print("*");
            }
            System.out.println();
        }
    }
    // 자동정렬(Ctrl + Alt + L)
}
